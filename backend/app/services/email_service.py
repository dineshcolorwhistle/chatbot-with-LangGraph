        html_body = f"""
        <html>
        <body style="font-family: Arial, sans-serif; line-height: 1.6; color: #333;">
            <div style="max-width: 600px; margin: 0 auto; border: 1px solid #ddd; padding: 20px; border-radius: 8px;">
                <h2 style="color: #2196F3;">Thank you!</h2>
                <p>Hi there,</p>
                <p>We've successfully received your project details and requirements through our website's project consultant chatbot.</p>
                <p>Our expert team at <strong>{company_name}</strong> is currently reviewing your needs. One of our technical representatives will get in touch with you shortly to schedule an initial discussion.</p>
                
                <p>Have a great day!</p>
                
                <div style="margin-top: 35px; border-top: 1px solid #ddd; padding-top: 15px;">
                    <strong>{company_name} Consultant Team</strong><br>
                    <a href="mailto:info@{company_name.lower().replace(' ', '')}.com" style="color: #2196F3;">Contact Support</a>
                </div>
            </div>
        </body>
        </html>
        """
        await cls._send_email(visitor_email, subject, html_body)
